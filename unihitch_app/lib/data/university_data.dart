  static Map<String, dynamic>? getUniversityByDomain(String email) {
    for (var uni in universities) {
      final dominios = uni['dominios'] as List<String>;
      for (var dominio in dominios) {
        if (email.endsWith(dominio)) {
          return uni;
        }
      }
    }
    return null;
  }

  static List<String> getCarreras(int universityId) {
    final uni = universities.firstWhere(
      (u) => u['id'] == universityId,
      orElse: () => {'carreras': <String>[]},
    );
    return List<String>.from(uni['carreras']);
  }
